package javaprograms;

public class Primenumber 
{

	public static void main(String[] args)
	
	{
	
		
			

			int n=97;
			for(int i=2;i<=n-1;i++)
				
			{
				if(i/n==0)
					
					
					
				{
					System.out.println("it is prime number" +n);
				}
				
				else
					
				{
					System.out.println("it is not a prime number" +n);
					
				}
			}
			
			
		}

		}

	


